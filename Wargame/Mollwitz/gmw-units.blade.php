<li><span class="big">Command Control</span>
    <ol style="counter-reset:item 5">
        <li>
            <span> Unit require command control to move at full value.</span>
            <p>At the beginning of each movement phase command control is determined. Units must be withing
            the command control radius of a HQ unit to be in command control.</p>
            <p>Units out of command control move at half movement allowance. They defend normally.</p>
            <p class="ruleComment">You may hover over an HQ to see it's command radius. Alternatively you may hit the "H" button in the header bar.</p>
        </li>
        <li>
            All HQ's have a command radius of 2 hexes.
        </li>
