<h2 class="exclusive">Victory Conditions</h2>
<ol>

    <li>
        <span class="big">How to determine the winner</span>
        <h4>The Game lasts 11 turns. Victory is determined at the end of the game. The player with the most victory
            points wins.</h4>
        <ol>
            <li>
                One victory point is awarded for each infantry strength point destroyed.
            </li>
            <li>
                Two victory points are awarded for each armored strength point destroyed.
            </li>
            <li>The player that holds any of the pakistani towns is awarded 3 points. The Pakistani player starts
                the game with 48 victory points for each of these cities that they hold.
            </li>

            <li>The Pakistani player only, is awarded 5 victory points for any indian cities they hold. If the Indian
                play retakes these
                cities, the Pakistani player loses the victory points.
            </li>
            <li>The Indian player receives no points for holding their own cities.</li>
        </ol>
    </li>

</ol>