    <h2>Victory Conditions</h2>
    <h4>The Game lasts 7 turns. Victory is determined at the end of the game.</h4>
    <ul>
        <li style="margin-bottom:20px">
            If the Rebel player holds any of the 3 hexes that compose Cuneiform, they win the game. The level of victory is detailed below.
            <ol>
                <li>
                    If the rebel player hold one hex, they win a marginal victory.
                </li>
                <li>
                    If the rebel player holds two hexes, they win a substantive victory.
                </li>
                <li>
                    If the rebel player holds all 3 hexes, they win a decisive victory.
                </li>
            </ol>
        </li>


        <li>
            If the Rebel player holds no hexes of Cuneiform at the end of the game, the loyalists win. The level of victory is details below.
            <ol>
                <li>
                    If the rebel player entered any hex at any time during the game, the loyalists win a marginal victory.
                </li>
                <li>
                    If the rebel player never entered Cuneiform at any time during the game, the loyalist win a substantive victory.
                </li>
                <li>
                    if the rebel player never entered Cuneiform OR any of the heavy fortified hex (the dark gray ones) the loyalist win a decisive victory.

                </li>
            </ol>
        </li>


    </ul>
