    <h2>Setting Up</h2>

    <ul>
        <li>The Russian player sets up first. The Russian units are red and green.</li>
        <li>They may deploy any or all their units on or inside the ring of "R"'s in the center of the map.
            <p>Clicking on a unit will
        display all possible placements of the unit. The Austrians may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
        movement phase. (it will cost their entire movement allowance though).
        </P></li>
        <li>When the Russian play is done deploying they should hit the "next phase" button</li>

        <li>The Prussian Player sets up after the Russian player is finished setting up. The Prussian units are blue and yellow. </li>
        <li>When the Prussian deploys their first unit, a coin is flipped and they are given either the "P1" hexes or "P2" hexes to deploy
        into.</li>
        <li> They may deploy any or all their units on or outside the P1 or P2 hexes chosen above.<p>

                Again, clicking on a unit will
                display all possible placements of the unit. The Prussians may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
            movement phase. (it will cost their entire movement allowance though).
            </P>
        </li>

    </ul>

    <h2>Victory Conditions</h2>
    <h4>The Game ends instantly at the end of any Player Turn when one or more of the following conditions applies. Mutual
        victories are a Draw</h4>
    <ol>
        <li> At the end of any turn that one player has accumulated 62 Victory Points with a 10 point lead.
            <p>Victory Points are awarded when one player destroys another players unit.</P>
            <ul>
                <li>
                    1 <abbr title="Victory Point">VP</abbr> per infantry or cavalry strength point killed.
                </li>
                <li>
                    2 <abbr title="Victory Point">VP</abbr> per artillery strength point killed.
                </li>
            </ul
            Victory Points are display in the Victory: box at the top of the page.
        </li>

        <li>If the prussians occupy or were the last to pass through any hexes of Quartschen or Zicher they will have 5 points per
            hex, for a total of up to 30 points.
            These points are awarded when the Prussian player first enters the hex.
            If the Russian player retakes any of these hexes the prussians lose the 5 points gained.If the Prussian player retakes the hex, they
            will be awarded the 5 points again.
        </li>

        <li>If at the end of any Prussian player turn there is an Austrian unit in Neudorf, the Austrians Win</li>


        <li>If at the end of any player turn both sides have somehow met their victory conditions above, the result is
            a draw.
        </li>

        <li>Turn 12 concludes with none of the above conditions met. Draw</li>
    </ol>
