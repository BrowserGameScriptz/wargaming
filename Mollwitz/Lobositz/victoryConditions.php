    <span class="lessBig">Victory Conditions</span>
    <h4>The Game ends instantly at the end of any Player Turn when one or more of the following conditions applies. Mutual
        victories are a Draw</h4>
    <ol>
        <li> At the end of any turn that one player has accumulated 60 Victory Points with a 10 point lead.
            <p class="ruleComment">Victory Points are awarded when one player destroys another players unit.</P>

                <li>
                    1 <abbr title="Victory Point">VP</abbr> per infantry strength point killed.
                </li>
                <li>
                    2 <abbr title="Victory Point">VP</abbr> per artillery or cavalry strength point killed.
                </li>
                <p class="ruleComment">Victory Points are display in the Victory: box at the top of the page.</p>
        </li>


        <li>If the Prussian occupy or were the last to pass through any hexes of the two road hexes exiting the south of the map (marked with a tag that says Austrian) at the end of an Austrian
            player turn. The Prussian Wins, regardless of VP's.
        </li>
        
        <li>If the Austrian occupy or were the last to pass through any hexes of the two road hexes exiting the north of the map (marked with a tag that says Prussian) at the end of an Prussian
            player turn. The Austrian player wins, regardless of VP's.
        </li>

        <li>
            If any of the above conditions arise such that both players win, the result is a draw.
        </li>

        <li>If turn 14 concludes with none of the above conditions met. The game is a draw.</li>
    </ol>
</li>