    <h2>Setting Up</h2>

    <ul>
        <li>The Anglo Allied player sets up first. The Anglo Allied units are red and white.</li>
        <li>They may deploy any or all their units on or north of the hexes with an A on them.
            <p>Clicking on a unit will
        display all possible placements of the unit. The Anglo Allieds may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
        movement phase. (it will cost their entire movement allowance though).
        </P></li>
        <li>When the Anglo Allied play is done deploying they should hit the "next phase" button</li>

        <li>The Prussian Player sets up after the Anglo Allied player is finished setting up. The Prussian units are blue and yellow. </li>
        <li> They may deploy any or all their units on or south of the hexes with an P on them.<p>

                Again, clicking on a unit will
                display all possible placements of the unit. The Prussian may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
            movement phase. (it will cost their entire movement allowance though).
            </P>
        </li>

    </ul>

    <h2>Victory Conditions</h2>
    <h4>The Game ends instantly at the end of any Player Turn when one or more of the following conditions applies. Mutual
        victories are a Draw</h4>
    <ol>
        <li> At the end of any turn that one player has accumulated 60 Victory Points with a 10 point lead.
            <p>Victory Points are awarded when one player destroys another players unit.</P>
            <ul>
                <li>
                    1 <abbr title="Victory Point">VP</abbr> per infantry strength point killed.
                </li>
                <li>
                    2 <abbr title="Victory Point">VP</abbr> per artillery or cavalry strength point killed.
                </li>
            </ul>
                Victory Points are display in the Victory: box at the top of the page.
        </li>


        <li>If the Prussian occupy or were the last to pass through any hexes of the two road hexes exiting the south of the map (marked with a tag that says Austrian) at the end of an Austrian
            player turn. The Prussian Wins, regardless of VP's.
        </li>
        
        <li>If the Austrian occupy or were the last to pass through any hexes of the two road hexes exiting the north of the map (marked with a tag that says Prussian) at the end of an Austrian
            player turn. The Prussian Wins, regardless of VP's.
        </li>

        <li>
            If any of the above conditions arise such that both players win, the result is a draw.
        </li>

        <li>If turn 14 concludes with none of the above conditions met. The game is a draw.</li>
    </ol>
