    <h2>Setting Up</h2>

    <ul>
        <li>The Anglo Allied player sets up first. The Anglo Allied units are red and white.</li>
        <li>They may deploy any or all their units on or north of the hexes with an A on them.
            <p>Clicking on a unit will
        display all possible placements of the unit. The Anglo Allieds may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
        movement phase. (it will cost their entire movement allowance though).
        </P></li>
        <li>When the Anglo Allied play is done deploying they should hit the "next phase" button</li>

        <li>The French Player sets up after the Anglo Allied player is finished setting up. The French units are blue and yellow. </li>
        <li> They may deploy any or all their units on or south of the hexes with an P on them.<p>

                Again, clicking on a unit will
                display all possible placements of the unit. The French may deploy all, some or none of their units.</p>
            <p></p>
            Units not deployed may be placed later during a subsequent
            movement phase. (it will cost their entire movement allowance though).
            </P>
        </li>

    </ul>

    <h2>Victory Conditions</h2>
    <h4>The Game ends instantly at the end of any Player Turn when one or more of the following conditions applies. Mutual
        victories are a Draw</h4>
    <ol>
        <li> At the end of any turn that one player has accumulated 50 Victory Points with a 10 point lead.
            <p>Victory Points are awarded when one player destroys another players unit.</P>
            <ul>
                <li>
                    1 <abbr title="Victory Point">VP</abbr> per infantry strength point killed.
                </li>
                <li>
                    2 <abbr title="Victory Point">VP</abbr> per artillery or cavalry strength point killed.
                </li>
            </ul>
                Victory Points are display in the Victory: box at the top of the page.
        </li>


        <li>If the French occupy or were the last to pass through any hexes of the 5 hexes with a 5 in them they will have 5 points per
            hex, for a total of up to 25 points. These points are all behind the starting line for the Anglo Allied player, and have a small tag that says "Anglo Allied" on them.
            These points are awarded when the French player first enters the hex.
            If the Anglo Allied player retakes any of these hexes the French lose the 5 points gained. If the French player retakes the hex, they
            will be awarded the 5 points again.
        </li>
        
        <li>If the Anglo Allied occupy or were the last to pass through any of the three hexes of Minden, (marked with a 10 on them), they will be awarded 10 points per
            hex, for a total of up to 30 points. These points are all behind the starting line for the French player, and have a small tag that says "Fremcj" on them.
            These points are awarded when the French player first enters the hex.
            If the French player retakes any of these hexes the French lose the 10 points gained. If the Anglo Allied player retakes the hex, they
            will be awarded the 10 points again.
        </li>

        <li>If turn 14 concludes with none of the above conditions met. The game is a draw.</li>
    </ol>
