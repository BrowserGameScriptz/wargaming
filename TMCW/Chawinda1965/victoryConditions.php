<span class="big">Exclusive Rules</span>
<ol>
    <li>
        <span class="big">Setting Up</span>
        <ol>
            <li>The Indian player setups up first. They setup on or to the east the the line of red 'I' characters on the map.</li>
            <p class="ruleComment">To place a unit, click on one of the units in the deploy staging area.
                You will see a cloud of possible positions to place the unit.</p>
            <p class="ruleComment">Once a unit has been placed on the map, it may be moved elsewhere by clicking on the unit again.</p>
            <li>Once the Indian player has deployed all their units, they click on the 'Next Phase' button.</li>
            <li>The Pakistani player sets up next. They place their units west of the line of red 'I' characters on the map.</li>
            <li>Once the Pakistani player completes their deployment, they click on 'Next Phase'. </li>
            <li>The Indian play is the first player and moves first.</li>
        </ol>
    </li>
    <li><span class="big">Special Rules</span>

    <ol>
        <li>Pakistani infantry are doubled on defense</li>
    </ol></li>
    <li>
        <span class="big">Victory Conditions</span>
        <h4>The Game lasts 11 turns. Victory is determined at the end of the game. The player with the most victory points wins.</h4>
        <ol>
            <li>
                One victory point is awarded for each infantry strength point destroyed.
            </li>
            <li>
                Two victory points are awarded for each armored strength point destroyed.
            </li>
            <li>The player that holds any of the pakistani towns is awarded 3 points. The Pakistani player starts
            the game with 48 victory points for each of these cities that they hold.</li>

            <li>The Pakistani player only, is awarded 5 victory points for any indian cities they hold. If the Indian play retakes these
            cities, the Pakistani player loses the victory points.</li>
            <li>The Indian player receives no points for holding their own cities.</li>
        </ol>
    </li>

</ol>